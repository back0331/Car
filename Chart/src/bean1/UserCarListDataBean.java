package bean1;

public class UserCarListDataBean {
	
	private String car_id;
	private String id;
	private String myCarName;
	private int agency_no;
	private String startDate;
	private String endDate;
	private String checkDate;
	private String state;
	private String reg_date;
	
	public String getCar_id() {
		return car_id;
	}
	public void setCar_id(String car_id) {
		this.car_id = car_id;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMyCarName() {
		return myCarName;
	}
	public void setMyCarName(String myCarName) {
		this.myCarName = myCarName;
	}
	public int getAgency_no() {
		return agency_no;
	}
	public void setAgency_no(int agency_no) {
		this.agency_no = agency_no;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getCheckDate() {
		return checkDate;
	}
	public void setCheckDate(String checkDate) {
		this.checkDate = checkDate;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	
	
}
